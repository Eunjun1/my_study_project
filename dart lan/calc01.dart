class Calc01 {
  // property
  int num1 = 0;
  int num2 = 0;

  // Construct(생성자)

  // Method
  int addition() {
    return num1 + num2;
  }

  int subtraction() {
    return num1 - num2;
  }

  int multiplication() {
    return num1 * num2;
  }

  double division() {
    return num1 / num2;
  }
}
