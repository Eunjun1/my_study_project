class Calc02 {
  // property
  // Construct(생성자)

  // Method
  int addition(num1, num2) {
    return num1 + num2;
  }

  int subtraction(num1, num2) {
    return num1 - num2;
  }

  int multiplication(num1, num2) {
    return num1 * num2;
  }

  double division(num1, num2) {
    return num1 / num2;
  }
}
